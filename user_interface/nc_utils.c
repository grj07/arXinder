void typewriter(char *typedText, int timeStep){
/*Types out from cursor position with gap timestep in milliseconds*/
	char *ch;
	ch = typedText;
	while(*ch){
		addch(*pos);
		pos++;
		refresh();
		usleep(timestep*1000);
	}
}

